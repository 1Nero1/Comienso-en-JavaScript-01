// Creando un if

// En este punto sabemos que un if es una condicion con eso nos referimos a que es una restriccion quedara mas claro connel soguiente ejemplo.

const valor = 1000;

if(valor === 1000){
    console.log(`Su cuenta es perfecta ${valor}`);
} else{
    console.log(`Su cuenta no llego al numero deseado`);
}