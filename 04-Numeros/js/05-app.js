// Incremento y decremento
// para incrementar los valores se pueden usar don formas

// Incremento
let incremento = 5;
console.log(incremento);
// Primer incremento; esta forma primrto muestra el valor inicial y cuando se vurlva a ejecutar purdta el incremento
incremento ++;
console.log(incremento);

// esta es otra forma de incrementar
++ incremento;
console.log(incremento);

// Decremento
let decremento = 10;
console.log(decremento);

// primer forma de decrementar
decremento --;
console.log(decremento);

// segunda forma
-- decremento;
console.log(decremento);

// No necesariamnete tiene que ser uno en uno tambien se puede de otras formas ejemplos
decremento -= 5;
console.log(decremento);


// o para sumar es igual
decremento += 5;
console.log(decremento);
