// Creando un Objeto
// TODO Un "Objeto" es como una varienle pero el "Objeto" agrupa todo en una sola variable
// Para crear un objeto es como una variable pero mas amplia
// Ejemplo:
const objeto = { // A esto se le conoce como Object literal
    valorUno: "Uno", // A esto se le conoce como propiedad o llave del Objeto
    valorDos: 2,
    valorTres: true
}
console.log(objeto);

